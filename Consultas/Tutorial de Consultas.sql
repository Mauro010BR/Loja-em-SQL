--TUTORIAL DE CONSULTAS

SELECT -- Para Ver uma Coluna
FROM -- Para ver uma Tabela
WHERE -- Para uma Condição
DISTINCT -- Para não repetir valores
COUNT -- Para contagem
TOP -- Para aparecer um numero determinado de linhas
Order By -- Para ordenar a ordem das Linhas (Crescente(asc),Decrescente(Desc))
Between -- Para determinar um valor nas linhas, por exemplo(Preços de camisa de 100RS a 200 RS)
IN -- Para aparecer somente colunas desejadas
LIKE -- Para valores aproximados
Min -- Valor minimo
Max -- Valor maximo
SUM -- Para somar
Avg -- Para média
Group By -- Para agrupar em uma condição, por exemplo(Agrupar pela soma dos preços)
HAVING -- Where para o Group By
As -- Para nomear uma coluna
Inner Join -- Para juntar duas tabelas com valores em comum
Left Join -- Tabela da esquerda com todos valores e da direita com valores em comum
Right Join -- Tabela da direita com todos valores e da esquerda com valores em comum
Full Outer Join -- Todos valores das Duas Tabelas
Union -- Unir duas tabelas com Select em comum
Self Join -- Para comparar linhas iguais, Exemplo (Funcionários que Entraram na empresa no mesmo Mês)
Subquery -- Para fazer um Select dentro de outro Select
DatePart -- Para aparecer o Dia,Mês ou Ano de uma data
Concat -- Para juntar duas colunas, Exemplo (Coluna Nome e Coluna Sobrenome)
Len -- Para contar quantas caracteres tem na palavra
Lower -- Para deixar a Coluna em mínusculo
Upper -- Para deixar a Coluna em Maiusculo
Substring -- Abreviar
Replace -- Para substituir valor
